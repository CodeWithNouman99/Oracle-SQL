1. Data:
Raw, unprocessed facts and figures without any context. Example: "Ali", 10000, 2025.

2. Information:
Processed or organized data that is meaningful and useful for decision-making. Example: "Ali has a salary of 10000".

3. DB (Database):
A structured collection of related data stored electronically, which can be easily accessed, managed, and updated.

4. MS (Management System):
A set of software tools or programs used to manage databases. It handles operations like storing, modifying, and retrieving data efficiently.

5. RDBMS (Relational Database Management System):
A type of database management system that stores data in the form of related tables using rows and columns. It supports SQL and ensures data integrity and relationships.

6. Oracle:
A powerful RDBMS developed by Oracle Corporation, used widely in enterprise environments to store, retrieve, and manage large amounts of structured data.

7. SQL (Structured Query Language):
A standard language used to communicate with relational databases. It is used to perform tasks such as querying data, updating records, and managing database structures.
